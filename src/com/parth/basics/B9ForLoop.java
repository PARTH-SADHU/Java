package com.parth.basics;

public class B9ForLoop {
    public static void main(String[] args) {

//        for loop - executes a block of code for a limited amount of time.
        for(int i=0; i<=10; i++){
            System.out.println(i);
        }

        for(int i=10; i>=0;i--){
            System.out.println(i);
        }
        System.out.println("Happy New Year");
    }
}
